var x1=1;
$("#question1").click(function(){
  if(x1==1){
    $("#answer1").removeClass("disp-none");
    x1=0;
  $("#answer1").addClass("disp-show");
}
else {
  $("#answer1").addClass("disp-none");
  x1=1;
$("#answer1").removeClass("disp-show");
}
});

